money_capital = 20000  # Подушка безопасности
salary = 5000  # Ежемесячная зарплата
spend = 6000  # Траты за первый месяц
increase = 0.05  # Ежемесячный рост цен

# Инициализация переменных
months = 0  # Счетчик месяцев
current_capital = money_capital  # Текущий капитал (подушка безопасности)

# Продолжаем жить пока хватает денег
while True:
    # Бюджет текущего месяца: зарплата + остаток подушки
    monthly_budget = salary + current_capital

    # Если траты превышают бюджет - останавливаемся
    if spend > monthly_budget:
        break

    # Уменьшаем подушку на разницу между тратами и зарплатой
    current_capital -= (spend - salary)

    # Увеличиваем счетчик месяцев
    months += 1

    # Увеличиваем траты на следующий месяц (рост цен)
    spend *= (1 + increase)

print("Количество месяцев, которое можно протянуть без долгов:", months)
